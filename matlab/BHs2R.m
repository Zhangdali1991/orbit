function Rvec=BHs2R(B,H,s,ae,ee)
%% 功能：由测站纬度,高程,恒星时求测站在地心惯性坐标系内的坐标
%% 调用： Rvec=BHs2R(B,H,s,ae,ee);
%% 输入：
%     B：测站的大地纬度
%     H：测站高程
%     s：测站恒星时
%     ae：地球半长轴
%     ee:偏心率
%% 输出：
%       R:[x;y;z]测站坐标
%% 测试：已通过
%      命令：   BHs2R(20*pi/180,1000,155*pi/180,6378.137e3,0.081819)
%      结果：   [-5.434924845329139; 2.534347076873849;2.168038872161098]
%%
xE=(ae/sqrt(1-(ee*sin(B))^2)+H)*cos(B);
zE=(ae*(1-ee^2)/sqrt(1-(ee*sin(B))^2)+H)*sin(B);
Rvec=[xE*cos(s);xE*sin(s);zE];
end